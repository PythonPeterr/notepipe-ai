@router.get("/zoho/callback")
async def zoho_callback(
    code: str = Query(...),
    state: str = Query(""),
    settings: Settings = Depends(get_settings),
    supabase: Client = Depends(get_supabase),
    accounts_server: str = Query("https://accounts.zoho.com", alias="accounts-server"),
    location: str = Query("us"),
) -> RedirectResponse:
    """Exchange the Zoho auth code for tokens and store the connection.

    Zoho returns accounts-server and location params in the callback to
    identify the user's data center. We store these for token refresh.
    """
    user_id = _verify_oauth_state(state, settings.supabase_service_role_key)

    # Validate accounts_server is a legitimate Zoho domain (prevent SSRF)
    allowed_zoho_hosts = {
        "accounts.zoho.com", "accounts.zoho.eu", "accounts.zoho.in",
        "accounts.zoho.com.cn", "accounts.zoho.com.au", "accounts.zoho.jp",
    }
    try:
        parsed_server = urllib.parse.urlparse(accounts_server)
        if parsed_server.hostname not in allowed_zoho_hosts:
            raise HTTPException(
                status_code=400, detail="Invalid Zoho accounts server"
            )
    except Exception:
        raise HTTPException(status_code=400, detail="Invalid Zoho accounts server")

    # Exchange code for tokens — use the user's accounts server
    async with httpx.AsyncClient(timeout=15.0) as client:
        response = await client.post(
            f"{accounts_server}/oauth/v2/token",
            data={
                "grant_type": "authorization_code",
                "client_id": settings.zoho_client_id,
                "client_secret": settings.zoho_client_secret,
                "redirect_uri": settings.zoho_redirect_uri,
                "code": code,
            },
        )

    if response.status_code != 200:
        logger.error("Zoho token exchange failed: %s", response.text)
        raise HTTPException(
            status_code=400,
            detail="Failed to authenticate with Zoho. Please try again.",
        )

    tokens = response.json()
    expires_at = (
        datetime.now(timezone.utc) + timedelta(seconds=tokens.get("expires_in", 3600))
    ).isoformat()

    api_domain = tokens.get("api_domain", "https://www.zohoapis.com")

    connection_data = {
        "user_id": user_id,
        "service": "zoho",
        "access_token": tokens["access_token"],
        "refresh_token": tokens.get("refresh_token"),
        "token_expires_at": expires_at,
        "metadata": {
            "accounts_server": accounts_server,
            "api_domain": api_domain,
            "location": location,
        },
        "updated_at": datetime.now(timezone.utc).isoformat(),
    }

    existing = (
        supabase.table("connections")
        .select("id")
        .eq("user_id", user_id)
        .eq("service", "zoho")
        .execute()
    )

    if existing.data:
        supabase.table("connections").update(connection_data).eq(
            "id", existing.data[0]["id"]
        ).execute()
    else:
        supabase.table("connections").insert(connection_data).execute()

    redirect_url = f"{settings.frontend_url}/connections?connected=zoho"
    return RedirectResponse(url=redirect_url)
