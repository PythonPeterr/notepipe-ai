logger = logging.getLogger(__name__)

router = APIRouter(prefix="/auth", tags=["auth"])

# OAuth state expires after 10 minutes
_STATE_MAX_AGE_SECONDS = 600


def _sign_oauth_state(user_id: str, secret: str) -> str:
    """Create a signed OAuth state: user_id:timestamp:signature.

    Prevents CSRF by ensuring the callback state was generated by us
    and hasn't been tampered with.
    """
    ts = str(int(time.time()))
    payload = f"{user_id}:{ts}"
    sig = hmac.new(secret.encode(), payload.encode(), hashlib.sha256).hexdigest()[:16]
    return f"{payload}:{sig}"


def _verify_oauth_state(state: str, secret: str) -> str:
    """Verify and extract user_id from a signed OAuth state.

    Returns:
        The user_id if valid.

    Raises:
        HTTPException: If state is invalid, expired, or tampered with.
    """
    parts = state.split(":")
    if len(parts) != 3:
        raise HTTPException(status_code=400, detail="Invalid OAuth state")

    user_id, ts_str, sig = parts

    # Check signature
    payload = f"{user_id}:{ts_str}"
    expected_sig = hmac.new(
        secret.encode(), payload.encode(), hashlib.sha256
    ).hexdigest()[:16]
    if not hmac.compare_digest(sig, expected_sig):
        raise HTTPException(status_code=400, detail="Invalid OAuth state signature")

    # Check expiry
    try:
        ts = int(ts_str)
    except ValueError:
        raise HTTPException(status_code=400, detail="Invalid OAuth state timestamp")

    if time.time() - ts > _STATE_MAX_AGE_SECONDS:
        raise HTTPException(status_code=400, detail="OAuth state expired")

    return user_id


# ---------------------------------------------------------------------------
# HubSpot OAuth
# ---------------------------------------------------------------------------

HUBSPOT_SCOPES = [
    "crm.objects.contacts.write",
    "crm.objects.contacts.read",
    "crm.objects.companies.write",
    "crm.objects.companies.read",
    "crm.objects.deals.write",
    "crm.objects.deals.read",
]


@router.get("/hubspot")
async def hubspot_auth(
    user: Any = Depends(get_current_user),
    settings: Settings = Depends(get_settings),
) -> RedirectResponse:
    """Redirect the user to HubSpot's OAuth consent screen."""
    params = {
        "client_id": settings.hubspot_client_id,
        "redirect_uri": settings.hubspot_redirect_uri,
        "scope": " ".join(HUBSPOT_SCOPES),
        "state": _sign_oauth_state(user.id, settings.supabase_service_role_key),
    }
    url = f"https://app.hubspot.com/oauth/authorize?{urllib.parse.urlencode(params)}"
    return RedirectResponse(url=url)


@router.get("/hubspot/callback")
async def hubspot_callback(
    code: str = Query(...),
    state: str = Query(""),
    settings: Settings = Depends(get_settings),
    supabase: Client = Depends(get_supabase),
) -> RedirectResponse:
    """Exchange the HubSpot auth code for tokens and store the connection."""
    user_id = _verify_oauth_state(state, settings.supabase_service_role_key)

    # Exchange code for tokens
    async with httpx.AsyncClient(timeout=15.0) as client:
        response = await client.post(
            "https://api.hubapi.com/oauth/v3/token",
            data={
                "grant_type": "authorization_code",
                "client_id": settings.hubspot_client_id,
                "client_secret": settings.hubspot_client_secret,
                "redirect_uri": settings.hubspot_redirect_uri,
                "code": code,
            },
        )

    if response.status_code != 200:
        logger.error("HubSpot token exchange failed: %s", response.text)
        raise HTTPException(
            status_code=400,
            detail="Failed to authenticate with HubSpot. Please try again.",
        )

    tokens = response.json()
    expires_at = (
        datetime.now(timezone.utc) + timedelta(seconds=tokens.get("expires_in", 21600))
    ).isoformat()

    # Upsert connection
    connection_data = {
        "user_id": user_id,
        "service": "hubspot",
        "access_token": tokens["access_token"],
        "refresh_token": tokens["refresh_token"],
        "token_expires_at": expires_at,
        "metadata": {},
        "updated_at": datetime.now(timezone.utc).isoformat(),
    }

    # Check if connection already exists
    existing = (
        supabase.table("connections")
        .select("id")
        .eq("user_id", user_id)
        .eq("service", "hubspot")
        .execute()
    )

    if existing.data:
        supabase.table("connections").update(connection_data).eq(
            "id", existing.data[0]["id"]
        ).execute()
    else:
        supabase.table("connections").insert(connection_data).execute()

    redirect_url = f"{settings.frontend_url}/connections?connected=hubspot"
    return RedirectResponse(url=redirect_url)


